    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String control_no = jTextField2.getText();
        String emp = jComboBox1.getSelectedItem().toString();
        if(control_no.isEmpty() || emp.equalsIgnoreCase("Select Personnel")){
            JOptionPane.showMessageDialog(this, "Error! Control No. and IT Staff required!", 
                    "Error Message", JOptionPane.ERROR_MESSAGE);
        }else{
            try{
                //check if control no. exists
                JRF jrf = jrfServ.getJRF_control_no(control_no);
                if(jrf == null){//not exists
                    JOptionPane.showMessageDialog(this, "Error! Control No.: "+control_no+" doesn't exists.", 
                            "Error Message", JOptionPane.ERROR_MESSAGE);
                }else{//exists
                    StringTokenizer token = new StringTokenizer(emp);
                    Person p = pServ.getPersonID(token.nextToken());
                    
                    if(isEdit == 0){
                        JobReqAssignment j = new JobReqAssignment();
                        j.setControl_no(control_no);
                        j.setAssigned_to(p);
                        j.setStatus(0);
                        jrfServ.editJob_req_assignment(j);
                        
                        //update status
                        JRF_status stat = new JRF_status();
                        stat.setControl_no(jrf);
                        stat.setStatus("Job Request assigned to "+p.getEmp_id());
                        jrfServ.updateJRF_status(stat);
                        
                        JOptionPane.showMessageDialog(this, "Job Request with Control No.: "+
                                control_no+" has been successfully assigned to "+p.getEmp_id()+".", 
                                "Information Message", JOptionPane.INFORMATION_MESSAGE);
                    }else{
                        JobReqAssignment j = new JobReqAssignment();
                        j.setControl_no(control_no);
                        j.setAssigned_to(p);
                        j.setStatus(0);
                        jrfServ.editJob_req_assignment(j);

                         //update status
                        JRF_status stat = new JRF_status();
                        stat.setControl_no(jrf);
                        stat.setStatus("JOb Request reassigned to "+p.getEmp_id());
                        jrfServ.updateJRF_status(stat);
                        
                        JOptionPane.showMessageDialog(this, "Job Request with Control No.: "+
                                control_no+" has been successfully updated.", 
                                "Information Message", JOptionPane.INFORMATION_MESSAGE);
                    }
                    jButton7.setEnabled(true);
                    jButton7.doClick();
                }
            }catch(ErrorException ex){}
        }
    }//GEN-LAST:event_jButton3ActionPerformed

