    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        index = -1;
        jButton2.setEnabled(false);
        jButton3.setEnabled(false);
        jButton7.setEnabled(false);
        
        if(jTextField1.isEnabled()){
            String search = jTextField1.getText();
        
            if(search.isEmpty()){
                JOptionPane.showMessageDialog(this, "Error! Please indicate the supplier name!", 
                        "Error Message", JOptionPane.ERROR_MESSAGE);
            }else{
                try{
                    supplierList.clear();
                    supplierList = suppServ.getSupplier(search);

                    if(supplierList.isEmpty()){
                        JOptionPane.showMessageDialog(this, "No data fetched for supplier name "+search+".", 
                            "Information Message", JOptionPane.INFORMATION_MESSAGE);
                    }
                    m.displaySuupliers(jList1, supplierList);
                }catch(ErrorException ex){ }
            }
        }else{
            jTextField1.setEnabled(true);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

