    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String control_no = jLabel5.getText();
        
        int del = JOptionPane.showConfirmDialog(this, "Are you sure to delete "
                + "Job Request with Control No.: "+control_no+" ?", 
                "Confirmation Message", JOptionPane.YES_NO_OPTION);
        
        if(del == 0){
            try{
                JRF jrf = new JRF();
                jrf.setControl_no(control_no);
                jrfServ.deleteJRF(jrf);
                
                JRF_status stat = new JRF_status();
                stat.setControl_no(jrf);
                jrfServ.deleteJRF_status(stat);
                JOptionPane.showMessageDialog(this, "Job Request with Control No.: "+
                        control_no+" has been deleted.", "Information Message", 
                        JOptionPane.INFORMATION_MESSAGE);
            }catch(ErrorException ex){}
            jButton6.setEnabled(true);
            jButton6.doClick();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

