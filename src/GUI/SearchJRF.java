    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        JRF jrf = jrfList.get(index);
        if(iden == 1){//get info
            t1.setText((jrf.getDate_app().getMonth()+1)+"/"+jrf.getDate_app().getDate()+
                    "/"+(jrf.getDate_app().getYear()+1900));
            t2.setText(jrf.getEmail());
            t3.setText(jrf.getContact());
            c1.setSelectedItem(jrf.getDept().getDept_code());
            t4.setText(jrf.getRoom());
            t5.setText(jrf.getRequested_by().getEmp_id());
            t6.setText(jrf.getRequested_by().getCompleteName());
            t7.setText(jrf.getRequested_by().getEmp_position());
            
            if(jrf.getJob_req_on() == 0)
                check1.setSelected(true);
            else if(jrf.getJob_req_on() == 1)
                check2.setSelected(true);
            else check3.setSelected(true);
            
            a1.setText(jrf.getJob_description());
            a2.setText(jrf.getAction_taken());
            t8.setText(jrf.getApproved_by().getEmp_id());
            t9.setText(jrf.getApproved_by().getCompleteName());
            t10.setText(jrf.getApproved_by().getEmp_position());
            l1.setText(jrf.getControl_no());
            
            b1.setEnabled(true);
            b2.setEnabled(true);
            b3.setEnabled(true);
        }else if(iden == 2){
            t11.setText(jrf.getControl_no());
            a3.setText(jrf.getAction_taken());
            
            t11.setEnabled(false);
            a3.setEnabled(false);
            b4.setEnabled(true);
        }else if(iden == 3){
            t12.setText(jrf.getControl_no());
        }
        this.dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

