    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String supp_name = jTextField1.getText();
        String supp_address = jTextArea1.getText();
        String supp_contact = jTextField2.getText();
        String supp_email = jTextField3.getText();
        
        if(supp_name.isEmpty() || supp_address.isEmpty()){
            JOptionPane.showMessageDialog(this, "Error! Please spicify at least Supplier name"
                    + " and address!", "Error Message", JOptionPane.ERROR_MESSAGE);
        }else{
            Supplier supp = new Supplier();
            if(isEdit == true){
                supp = suppTemp;
            }
            supp.setSupp_name(supp_name);
            supp.setSupp_address(supp_address);
            supp.setSupp_contact(supp_contact);
            supp.setSupp_email(supp_email);
            
            if(m.checkSupplier(supp) == 1 && isEdit == false){
                JOptionPane.showMessageDialog(this, "Error! Duplicate in supplier name!", 
                        "Error Message", JOptionPane.ERROR_MESSAGE);
            }else{
                try{
                    if(isEdit == false){
                        suppServ.addSupplier(supp);
                        JOptionPane.showMessageDialog(this, "Your have successfully added new supplier.", 
                            "Information Message", JOptionPane.INFORMATION_MESSAGE);
                        supplierList.add(supp);
                    }else{ 
                        suppServ.updateSupplier(supp);
                        JOptionPane.showMessageDialog(this, "Your have successfully updated the supplier.", 
                            "Information Message", JOptionPane.INFORMATION_MESSAGE);
                        isEdit = false;
                        supplierList.set(index, supp);
                    }
//                    clearFields();
//                    fields(false);
//                    jButton2.setEnabled(false);
//                    jButton3.setEnabled(false);
//                    jButton7.setEnabled(false);
                    jButton8.doClick();
                    m.displaySuupliers(jList1, supplierList);
                }catch(ErrorException e){ }
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

