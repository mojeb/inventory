    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String control_no = jTextField2.getText();
        String emp = jComboBox1.getSelectedItem().toString();
        
        int del = JOptionPane.showConfirmDialog(this, "Are you sure to delete Control No. "
                +control_no+" assigned to "+emp+" ?", "Confirmation Message", 
                JOptionPane.YES_NO_OPTION);
        
        if(del == 0){
            try{
                JobReqAssignment j = new JobReqAssignment();
                j.setControl_no(control_no);
                
                jrfServ.deleteJob_req_assignment(j);
                JOptionPane.showMessageDialog(this, "Job Request Control No. "+control_no
                        +" assigned to "+emp+" deleted.", "Information Message", 
                        JOptionPane.INFORMATION_MESSAGE);
                
                jButton7.setEnabled(true);
                jButton7.doClick();
            }catch(ErrorException ex){}
        }
    }//GEN-LAST:event_jButton5ActionPerformed

