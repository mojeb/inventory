    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String email = jTextField2.getText();
        String contact = jTextField3.getText();
        String room = jTextField4.getText();
        String office = jComboBox1.getSelectedItem().toString();
        String requested_by =  jTextField5.getText();
        String job_req_des = jTextArea1.getText();
        String action_taken = jTextArea2.getText();
        String approved_by = jTextField8.getText();
        
        if(office.equalsIgnoreCase("Select Office")){
            JOptionPane.showMessageDialog(this, "Error! Please select your office.", 
                    "Error Message", JOptionPane.ERROR_MESSAGE);
        }else{
            if(requested_by.isEmpty() || approved_by.isEmpty()){
                JOptionPane.showMessageDialog(this, "Error! Requested By and Approved By required.", 
                        "Error Message", JOptionPane.ERROR_MESSAGE);
            }else{
                if(!jCheckBox1.isSelected() && !jCheckBox2.isSelected() && !jCheckBox3.isSelected()){
                    JOptionPane.showMessageDialog(this, "Error! Job Request On required.", 
                            "Error Message", JOptionPane.ERROR_MESSAGE);
                }else{
                    if(job_req_des.isEmpty()){
                        JOptionPane.showMessageDialog(this, "Error! Job Request Description required.", 
                                "Error Message", JOptionPane.ERROR_MESSAGE);
                    }else{
                        try{
                            Person req_by = pServ.getPersonID(requested_by);
                            Person app_by = pServ.getPersonID(approved_by);
                            if(req_by == null){
                                JOptionPane.showMessageDialog(this, "Error! Employee with ID No.: "+requested_by+
                                        " doesn't exists.", "Error Message", JOptionPane.ERROR_MESSAGE);
                            }else if(app_by == null){
                                JOptionPane.showMessageDialog(this, "Error! Employee with ID No.: "+approved_by+
                                        " doesn't exists.", "Error Message", JOptionPane.ERROR_MESSAGE);
                            }else{
                                int job_req_on = 0;
                                if(jCheckBox2.isSelected())
                                    job_req_on = 1;
                                else if(jCheckBox3.isSelected())
                                    job_req_on = 2;
                                
                                Department dept = dServ.getDepartmentbyCode(office);
                                
                                JRF jrf = new JRF();
                                jrf.setControl_no(jLabel5.getText());
                                jrf.setEmail(email);
                                jrf.setContact(contact);
                                jrf.setDept(dept);
                                jrf.setRoom(room);
                                jrf.setRequested_by(req_by);
                                jrf.setJob_req_on(job_req_on);
                                jrf.setJob_description(job_req_des);
                                jrf.setAction_taken(action_taken);
                                jrf.setApproved_by(app_by);
                                
                                if(jLabel3.getText().equalsIgnoreCase("New Job Request")){
                                    jrf.setYear(m.getCurrentYear());
                                    int lastSeq = jrfServ.getLast_seq(m.getCurrentYear())+1;
                                    if(lastSeq == 0)
                                        jrf.setSeq(1);
                                    else jrf.setSeq(lastSeq);
                                    
                                    jrfServ.addJRF(jrf);
                                    //jrf status
                                    JRF_status stat = new JRF_status();
                                    stat.setControl_no(jrf);
                                    stat.setStatus("Job Request created by "+requested_by);
                                    jrfServ.addJRF_status(stat);
                                    
                                    //jrf assignment
                                    JobReqAssignment ass = new JobReqAssignment();
                                    ass.setControl_no(jrf.getControl_no());
                                    ass.setStatus(-1);
                                    jrfServ.addJob_req_assignment(ass);
                                    
                                    JOptionPane.showMessageDialog(this, "Job Request created with Control No.: "
                                            +jLabel5.getText()+".", "Information Message", 
                                            JOptionPane.INFORMATION_MESSAGE);
                                }else if(jLabel3.getText().equalsIgnoreCase("Update Job Request")){
                                    jrfServ.updateJRF(jrf);
                                    JOptionPane.showMessageDialog(this, "Job Request has been updated with Control No.: "
                                            +jLabel5.getText()+".", "Information Message", 
                                            JOptionPane.INFORMATION_MESSAGE);
                                }
                                setEnable(false);
                                jButton10.setEnabled(true);
                                jButton1.setEnabled(true);
                                jButton2.setEnabled(false);
                                jButton3.setEnabled(true);
                                jButton4.setEnabled(true);
                            }
                        }catch(ErrorException ex){}
                    }
                }
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

