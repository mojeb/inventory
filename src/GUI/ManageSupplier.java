    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int del = JOptionPane.showConfirmDialog(this, "Are you sure to delete \n"
                + suppTemp.getSupp_name()+"?", "Confirmation Message", 
                JOptionPane.YES_NO_OPTION);
        
        if(del == 0){
            try{
                suppServ.deleteSupplier(suppTemp);
                JOptionPane.showMessageDialog(this, "You successfully deleted the supplier \n"
                    + suppTemp.getSupp_name()+"?", "Information Message", 
                    JOptionPane.INFORMATION_MESSAGE);
            }catch(ErrorException e){ }
            
            jButton2.setEnabled(false);
            jButton3.setEnabled(false);
            jButton7.setEnabled(false);
            fields(false);
            clearFields();
            supplierList.remove(index);
            m.displaySuupliers(jList1, supplierList);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

