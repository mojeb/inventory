    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!jRadioButton1.isSelected() && !jRadioButton2.isSelected() && 
                !jRadioButton3.isSelected() && !jRadioButton4.isSelected()){
            JOptionPane.showMessageDialog(this, "Error! Please select search option.", 
                    "Error Message", JOptionPane.ERROR_MESSAGE);
        }else{
            try{
                if(jRadioButton1.isSelected()){
                    String dept_code = jComboBox1.getSelectedItem().toString();
                    if(dept_code.equalsIgnoreCase("Select Office")){
                        JOptionPane.showMessageDialog(this, "Error! Please select office properly.", 
                                "Error Message", JOptionPane.ERROR_MESSAGE);
                    }else{
                        Department d = dServ.getDepartmentbyCode(dept_code);
                        
                        jrfList = jrfServ.getJRF_Office(d.getDept_id());
                    }
                }else if(jRadioButton2.isSelected()){
                    String emp_id = jTextField1.getText();
                    if(emp_id.isEmpty()){
                        JOptionPane.showMessageDialog(this, "Error! Employee ID required.", 
                                "Error Message", JOptionPane.ERROR_MESSAGE);
                    }else{
                        Person p = pServ.getPersonID(emp_id);
                        if(p == null){
                            JOptionPane.showMessageDialog(this, "Error! Employee ID: "+emp_id+" doesn't exist.", 
                                    "Error Message", JOptionPane.ERROR_MESSAGE);
                        }else{
                            jrfList = jrfServ.getJRF_employee(emp_id);
                        }
                    }
                }else if(jRadioButton3.isSelected()){
                    String req_on_str = jComboBox2.getSelectedItem().toString();
                    int req_on;
                    if(req_on_str.equalsIgnoreCase("Hardware/OS"))
                        req_on = 0;
                    else if(req_on_str.equalsIgnoreCase("Network"))
                        req_on = 1;
                    else req_on = 2;
                    
                    jrfList = jrfServ.getJRF_Job_req_on(req_on);
                }else if(jRadioButton4.isSelected()){
//                    Date date = jDateChooser1.getDate();
//                    if(date == null){
//                        JOptionPane.showMessageDialog(this, "Error! Date required.", 
//                                "Error Message", JOptionPane.ERROR_MESSAGE);
//                    }else{
//                        String date_str = (date.getYear()+1900)+"-"+(date.getMonth()+1)+"-"+
//                                date.getDate();
//                        jrfList = jrfServ.getJRF_date(date_str);
//                    }
                }
                //display data table
                m.displayJRF((DefaultTableModel)jTable1.getModel(), jrfList);
            }catch(ErrorException ex){ex.printMessage();}
        }
    }//GEN-LAST:event_jButton1ActionPerformed

